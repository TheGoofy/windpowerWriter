#include "ggFont.h"
#include "ggFontBold.h"


byte ggFontBold::GetFontHeight() const
{
  return 8;
}

  
byte ggFontBold::GetCharWidth(char aChar) const
{
  return mChars[GetCharIndex(aChar)][8];
}

const byte* ggFontBold::GetCharPixels(char aChar) const
{
  return mChars[GetCharIndex(aChar)];
}
  

byte ggFontBold::GetCharIndex(char aChar) const
{
  byte vCharIndex = aChar - 32;
  return vCharIndex < mCharSize ? vCharIndex : mCharSize - 1;
}


